a = 10
b = 2
print("a = ", a)
print("b = ", b)
print("a + b = ", a + b) # сложение чисел
print("a - b = ", a - b)
print("a * b = ", a * b)
print("a / b = ", a / b)
print("a // b = ", a // b) # деление с округлением вниз до целого
print("a % b = ", a % b) # остаток от деления
print("a ** b = ", a ** b) # возведение в степень
# приведение типа int к float
a1 = float(a)
print("a1 = ",a1)
print("\n\n")

